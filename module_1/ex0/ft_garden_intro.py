#!/usr/bin/env python3

def main():
    print("=== Welcome to My Garden ===")
    name = "Rose"
    hight = 120  # in cm
    age = 45     # in days
    print(f"Plant: {name}")
    print(f"Height: {hight} cm")
    print(f"Age: {age} days\n")
    print(f"=== End of Program ===")

if __name__ == "__main__":
    main()

